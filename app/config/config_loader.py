def _expand_config_refs(config: Any) -> Any:
    """Expand structured reference markers in a loaded config structure.

    Supported syntax (must be valid JSON):
        { "$ref": "<CONSTANT_KEY>" }

    Constants are read from the top-level dict key "constants", e.g.:
        { "constants": { "$_BACKGROUND_COLOR_1": [45, 45, 50] }, ... }

    Notes:
    - If no "constants" section exists, this is a no-op.
    - Cycles and missing refs raise ValueError.
    """
    if not isinstance(config, dict):
        return config

    constants_raw = config.get("constants", {})
    if constants_raw is None:
        constants_raw = {}
    if not isinstance(constants_raw, dict):
        raise ValueError('"constants" must be a JSON object when present')

    constants: Dict[str, Any] = constants_raw
    resolving: Set[str] = set()
    resolved_cache: Dict[str, Any] = {}

    def _is_ref_marker(node: Any) -> Optional[str]:
        if not isinstance(node, dict):
            return None
        # Only treat as marker if it is exactly {"$ref": "..."} (no extra keys)
        if set(node.keys()) != {"$ref"}:
            return None
        ref_value = node.get("$ref")
        return ref_value if isinstance(ref_value, str) else None

    def _expand_node(node: Any) -> Any:
        ref_name = _is_ref_marker(node)
        if ref_name is not None:
            return _resolve_constant(ref_name)

        if isinstance(node, dict):
            return {k: _expand_node(v) for k, v in node.items()}
        if isinstance(node, list):
            return [_expand_node(v) for v in node]
        return node

    def _resolve_constant(name: str) -> Any:
        if name in resolved_cache:
            return resolved_cache[name]
        if name in resolving:
            chain = " -> ".join(list(resolving) + [name])
            raise ValueError(f"Cyclic constant reference detected: {chain}")
        if name not in constants:
            raise ValueError(f'Unknown constant reference: "{name}"')

        resolving.add(name)
        value = _expand_node(constants[name])
        resolving.remove(name)
        resolved_cache[name] = value
        return value

    return _expand_node(config)


def _strip_json_comments(text: str) -> str:
    """Strip // and /* */ comments from JSON-like text, preserving strings."""
    out: list[str] = []
    i = 0
    n = len(text)
    in_string = False
    string_quote = '"'
    escape = False
    in_line_comment = False
    in_block_comment = False

    while i < n:
        ch = text[i]
        nxt = text[i + 1] if i + 1 < n else ""

        if in_line_comment:
            if ch == "\n":
                in_line_comment = False
                out.append("\n")
            i += 1
            continue

        if in_block_comment:
            if ch == "*" and nxt == "/":
                in_block_comment = False
                i += 2
                continue
            if ch == "\n":
                out.append("\n")
            i += 1
            continue

        if in_string:
            out.append(ch)
            if escape:
                escape = False
            elif ch == "\\":
                escape = True
            elif ch == string_quote:
                in_string = False
            i += 1
            continue

        if ch in ('"', "'"):
            in_string = True
            string_quote = ch
            out.append(ch)
            i += 1
            continue

        if ch == "/" and nxt == "/":
            in_line_comment = True
            i += 2
            continue
        if ch == "/" and nxt == "*":
            in_block_comment = True
            i += 2
            continue

        out.append(ch)
        i += 1

    return "".join(out)


def _load_json_allowing_comments(path: Path) -> Any:
    with open(path, "r", encoding="utf-8") as f:
        raw = f.read()
    return json.loads(_strip_json_comments(raw))


def _deep_merge_dicts(base: Dict[str, Any], overlay: Dict[str, Any]) -> Dict[str, Any]:
    """Deep merge two dicts, with overlay taking precedence.

    - Dict values are merged recursively.
    - Non-dict values (including lists) are replaced by overlay.
    """
    merged: Dict[str, Any] = dict(base)
    for k, v in overlay.items():
        if k in merged and isinstance(merged[k], dict) and isinstance(v, dict):
            merged[k] = _deep_merge_dicts(merged[k], v)
        else:
            merged[k] = v
    return merged


def resolve_style_config_path(*, base_config_path: Path, style_ref: str) -> Path:
    """Resolve a style config path relative to config.json and repo root."""
    # 1) Relative to the directory containing config.json
    candidate = (base_config_path.parent / style_ref).resolve()
    if candidate.is_file():
        return candidate

    # 2) Relative to repo root (config.json is typically <repo>/app/config/config.json)
    try:
        repo_root = base_config_path.resolve().parents[2]
    except Exception:
        repo_root = base_config_path.parent
    candidate = (repo_root / style_ref).resolve()
    return candidate


def read_default_style_config_ref(config_path: Path) -> Optional[str]:
    """Read ``default_style_config`` from base ``config.json`` (no style merge).

    Used when falling back from a missing user-selected style file.
    """
    base = _load_json_allowing_comments(config_path)
    if not isinstance(base, dict):
        return None
    ref = base.get("default_style_config")
    if isinstance(ref, str) and ref.strip():
        return ref.strip()
    return None


def _load_merged_config(config_path: Path) -> Dict[str, Any]:
    """Load config.json and optionally merge a default style config."""
    base = _load_json_allowing_comments(config_path)
    if not isinstance(base, dict):
        raise TypeError("Config root must be a JSON object")

    style_ref = base.get("default_style_config")
    if isinstance(style_ref, str) and style_ref.strip():
        style_path = resolve_style_config_path(base_config_path=config_path, style_ref=style_ref.strip())
        if not style_path.is_file():
            raise FileNotFoundError(f'Default style config not found: "{style_path}" (from "{style_ref}")')
        style_cfg = _load_json_allowing_comments(style_path)
        if not isinstance(style_cfg, dict):
            raise TypeError("Style config root must be a JSON object")
        # style_cfg provides defaults; base overrides
        return _deep_merge_dicts(style_cfg, base)

    return base


def _load_merged_config_with_style_override(config_path: Path, style_ref: str) -> Dict[str, Any]:
    """Load config.json and merge a specific style config (override).

    This is used for runtime theme switching without mutating config.json on disk.
    The merge semantics match :func:`_load_merged_config`: style provides defaults,
    base config overrides.
    """
    base = _load_json_allowing_comments(config_path)
    if not isinstance(base, dict):
        raise TypeError("Config root must be a JSON object")

    if not isinstance(style_ref, str) or not style_ref.strip():
        return base

    style_path = resolve_style_config_path(base_config_path=config_path, style_ref=style_ref.strip())
    if not style_path.is_file():
        raise FileNotFoundError(f'Style config not found: "{style_path}" (from "{style_ref}")')
    style_cfg = _load_json_allowing_comments(style_path)
    if not isinstance(style_cfg, dict):
        raise TypeError("Style config root must be a JSON object")
    return _deep_merge_dicts(style_cfg, base)


def _apply_debug_random_placeholder_colors(config: Any) -> None:
    """Optionally override placeholder color constants with random RGB values.

    When enabled via config["debug"]["enable_debug_rand_colors"] == True,
    every constant key that looks like a color placeholder (e.g. "$_COLOR_*")
    is replaced with one random RGB triplet for that key (not per occurrence).
    This runs *before* $ref expansion so the randomized colors propagate
    everywhere the placeholder is referenced.
    """
    if not isinstance(config, dict):
        return

    debug_section = config.get("debug")
    if not isinstance(debug_section, dict):
        return

    if not debug_section.get("enable_debug_rand_colors", False):
        return

    constants = config.get("constants")
    if not isinstance(constants, dict):
        return

    # One random color per placeholder key, stable within this load() call.
    per_key_color: Dict[str, Tuple[int, int, int]] = {}
    rng = random.Random()

    for key, value in list(constants.items()):
        if not isinstance(key, str):
            continue
        if not key.startswith("$_COLOR_"):
            continue

        # Only override "color-like" constants (RGB triplets).
        if not (isinstance(value, list) and len(value) == 3):
            continue
        if not all(isinstance(v, int) for v in value):
            continue

        if key not in per_key_color:
            per_key_color[key] = (rng.randint(0, 255), rng.randint(0, 255), rng.randint(0, 255))
        r, g, b = per_key_color[key]
        constants[key] = [r, g, b]


