"""Loads and validates configuration with strict validation."""

import json
import sys
from pathlib import Path
from typing import Any, Dict


class ConfigLoader:
    """Strict configuration loader that fails fast on validation errors."""
    
    def __init__(self, config_path: Path | None = None) -> None:
        """Initialize the config loader.
        
        Args:
            config_path: Path to config.json. If None, uses default location.
        """
        if config_path is None:
            # Default: config.json in app/config directory
            config_dir = Path(__file__).parent
            config_path = config_dir / "config.json"
        
        self.config_path = config_path
        self._config: Dict[str, Any] = {}
    
    def load(self) -> Dict[str, Any]:
        """Load and validate configuration from file.
        
        Returns:
            Loaded configuration dictionary.
            
        Raises:
            FileNotFoundError: If config file doesn't exist.
            json.JSONDecodeError: If config file is not valid JSON.
            ValueError: If required configuration keys are missing.
        """
        if not self.config_path.exists():
            self._fail(f"Configuration file not found: {self.config_path}")
        
        if not self.config_path.is_file():
            self._fail(f"Configuration path is not a file: {self.config_path}")
        
        try:
            with open(self.config_path, "r", encoding="utf-8") as f:
                self._config = json.load(f)
        except json.JSONDecodeError as e:
            self._fail(f"Invalid JSON in configuration file: {e}")
        
        # Validate required keys
        self._validate()
        
        return self._config
    
    def _validate(self) -> None:
        """Validate that required configuration keys exist.
        
        Raises:
            ValueError: If required keys are missing.
        """
        required_keys = self._get_required_keys()
        missing_keys = []
        
        for key in required_keys:
            if not self._has_key(key):
                missing_keys.append(key)
        
        if missing_keys:
            self._fail(f"Missing required configuration keys: {', '.join(missing_keys)}")
    
