class ConfigLoader:
    """Strict configuration loader that fails fast on validation errors."""
    
    def __init__(self, config_path: Optional[Path] = None) -> None:
        """Initialize the config loader.
        
        Args:
            config_path: Path to config.json. If None, uses default location.
        """
        if config_path is None:
            # Default: config.json in app/config directory
            config_dir = Path(__file__).parent
            config_path = config_dir / "config.json"
        
        self.config_path = config_path
        self._config: Dict[str, Any] = {}
    
    def load(self) -> Dict[str, Any]:
        """Load and validate configuration from file.
        
        Returns:
            Loaded configuration dictionary.
            
        Raises:
            FileNotFoundError: If config file doesn't exist.
            json.JSONDecodeError: If config file is not valid JSON.
            ValueError: If required configuration keys are missing.
        """
        if not self.config_path.exists():
            self._fail(f"Configuration file not found: {self.config_path}")
        
        if not self.config_path.is_file():
            self._fail(f"Configuration path is not a file: {self.config_path}")
        
        try:
            self._config = _load_merged_config(self.config_path)
        except (OSError, json.JSONDecodeError, TypeError, FileNotFoundError) as e:
            self._fail(f"Invalid JSON in configuration file: {e}")

        # Debug: optionally replace placeholder colors with random colors
        # before $ref expansion so every placeholder key becomes distinct.
        _apply_debug_random_placeholder_colors(self._config)

        # Expand placeholder constants before strict validation
        try:
            self._config = _expand_config_refs(self._config)
        except ValueError as e:
            self._fail(str(e))
        
        # Validate required keys
        self._validate()
        
        # Log configuration loaded
        # NOTE: Pass config to get_instance to ensure logging service uses correct config
        try:
            from app.services.logging_service import LoggingService
            logging_service = LoggingService.get_instance(self._config)
            logging_service.info(f"Configuration loaded: path={self.config_path}, validation=passed")
        except Exception:
            pass  # Silently ignore if logging not available during config loading
        
        return self._config

    def load_with_style_override(self, style_ref: str) -> Dict[str, Any]:
        """Load config.json but force a specific style config for defaults.

        Args:
            style_ref: Style config path/reference (same resolution rules as default_style_config).
        """
        if not self.config_path.exists():
            self._fail(f"Configuration file not found: {self.config_path}")

        if not self.config_path.is_file():
            self._fail(f"Configuration path is not a file: {self.config_path}")

        try:
            self._config = _load_merged_config_with_style_override(self.config_path, style_ref)
        except (OSError, json.JSONDecodeError, TypeError, FileNotFoundError) as e:
            self._fail(f"Invalid JSON in configuration file: {e}")

        _apply_debug_random_placeholder_colors(self._config)

        try:
            self._config = _expand_config_refs(self._config)
        except ValueError as e:
            self._fail(str(e))

        self._validate()
        return self._config
    
    def _validate(self) -> None:
        """Validate that required configuration keys exist.
        
        Raises:
            ValueError: If required keys are missing.
        """
        required_keys = self._get_required_keys()
        missing_keys = []
        
        for key in required_keys:
            if not self._has_key(key):
                missing_keys.append(key)
        
        if missing_keys:
            self._fail(f"Missing required configuration keys: {', '.join(missing_keys)}")
    
    def _get_required_keys(self) -> list[str]:
        """Get list of required configuration keys.
        
        Returns:
            List of required key paths (e.g., ['version'] or ['ui.window.width']).
        """
        return list(_REQUIRED_CONFIG_KEY_PATHS)

    def _has_key(self, key_path: str) -> bool:
        """Check if a key path exists in the configuration.
        
        Args:
            key_path: Dot-separated path to the key (e.g., 'ui.window.width').
            
        Returns:
            True if the key exists, False otherwise.
        """
        keys = key_path.split('.')
        current = self._config
        
        for key in keys:
            if not isinstance(current, dict) or key not in current:
                return False
            current = current[key]
        
        return True
    
    def get(self, key_path: str, default: Any = None) -> Any:
        """Get a configuration value by key path.
        
        Args:
            key_path: Dot-separated path to the key (e.g., 'ui.window.width').
            default: Default value if key doesn't exist. If None, raises ValueError.
            
        Returns:
            Configuration value.
            
        Raises:
            ValueError: If key doesn't exist and default is None.
        """
        keys = key_path.split('.')
        current = self._config
        
        for key in keys:
            if not isinstance(current, dict) or key not in current:
                if default is not None:
                    return default
                self._fail(f"Configuration key not found: {key_path}")
            current = current[key]
        
        return current
    
    def _fail(self, message: str) -> None:
        """Fail immediately with error message.
        
        Args:
            message: Error message to display.
        """
        logged = False
        if _logging_available:
            try:
                logging_service = LoggingService.get_instance()
                logging_service.error(f"Configuration Error: {message}")
                logged = True
            except Exception:
                pass
        if not logged:
            print(f"Configuration Error: {message}", file=sys.stderr)
        sys.exit(1)

def read_ui_dialog_section(
    dialog_key: str,
    *,
    config_path: Optional[Path] = None,
) -> Optional[Dict[str, Any]]:
    """Load ``ui.dialogs.<dialog_key>`` from config.json on disk.

    Modal dialogs use this so width, height, and other keys reflect edits to the file
    without restarting the application. Startup still uses :meth:`ConfigLoader.load`.

    Returns:
        The dialog subsection dict, or None if the file is missing, JSON is invalid,
        or the section is absent or not a dict.
    """
    if config_path is None:
        config_path = Path(__file__).resolve().parent / "config.json"
    try:
        if not config_path.is_file():
            return None
        data = _load_merged_config(config_path)
        data = _expand_config_refs(data)
        section = data.get("ui", {}).get("dialogs", {}).get(dialog_key)
        return section if isinstance(section, dict) else None
    except (OSError, json.JSONDecodeError, TypeError):
        return None


