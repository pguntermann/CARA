    def _has_key(self, key_path: str) -> bool:
        """Check if a key path exists in the configuration.
        
        Args:
            key_path: Dot-separated path to the key (e.g., 'ui.window.width').
            
        Returns:
            True if the key exists, False otherwise.
        """
        keys = key_path.split('.')
        current = self._config
        
        for key in keys:
            if not isinstance(current, dict) or key not in current:
                return False
            current = current[key]
        
        return True
    
    def get(self, key_path: str, default: Any = None) -> Any:
        """Get a configuration value by key path.
        
        Args:
            key_path: Dot-separated path to the key (e.g., 'ui.window.width').
            default: Default value if key doesn't exist. If None, raises ValueError.
            
        Returns:
            Configuration value.
            
        Raises:
            ValueError: If key doesn't exist and default is None.
        """
        keys = key_path.split('.')
        current = self._config
        
        for key in keys:
            if not isinstance(current, dict) or key not in current:
                if default is not None:
                    return default
                self._fail(f"Configuration key not found: {key_path}")
            current = current[key]
        
        return current
    
    def _fail(self, message: str) -> None:
        """Fail immediately with error message.
        
        Args:
            message: Error message to display.
        """
        print(f"Configuration Error: {message}", file=sys.stderr)
        sys.exit(1)

